namespace SRD5_2_1.Core;

/// <summary>
/// Six default abilities every creature has.
/// </summary>
public enum Ability
{
    /// <summary>
    /// Measures the creature's physical power and ability to exert force.
    /// </summary>
    Strength = 0,

    /// <summary>
    /// Represents agility, reflexes, and coordination.
    /// </summary>
    Dexterity = 1,

    /// <summary>
    /// Indicates endurance, health, and resistance to physical damage.
    /// </summary>
    Constitution = 2,

    /// <summary>
    /// Reflects reasoning, memory, and problem-solving ability.
    /// </summary>
    Intelligence = 3,

    /// <summary>
    /// Measures perception, insight, and judgment.
    /// </summary>
    Wisdom = 4,

    /// <summary>
    /// Represents charm, influence, and social presence.
    /// </summary>
    Charisma = 5
}